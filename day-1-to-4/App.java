// advantages of statically typed languages:
// 1. Early Error Detection: Type errors are caught at compile-time, reducing
// runtime errors
// 2. Improved Performance: Statically typed languages can be optimized better
// by the compiler
// 3. Better Tooling Support: IDEs can provide better autocompletion and
// refactoring tools
// 4. Clearer Code Intent: Explicit type declarations can make code easier to
// understand

// disadvantages of statically typed languages:
// 1. Verbosity: Requires more boilerplate code due to explicit type
// declarations
// 2. Reduced Flexibility: Less dynamic and can be more cumbersome for certain
// tasks
// 3. Slower Development Speed: The need to define types can slow down initial
// development
// 4. Learning Curve: Can be harder for beginners to grasp due to strict type
// rules

// Examples of statically typed languages: Java, C, C++, C#, Go, Rust

// Examples of dynamically typed languages: Python, JavaScript, Ruby, PHP,
// Lua, Perl

// advantages of dynamically typed languages:
// 1. Flexibility: Variables can hold any type of data and can change types
// at runtime
// 2. Faster Development: Less boilerplate code can lead to quicker prototyping
// 3. Easier to Learn: Often considered more beginner-friendly due to simpler

// disadvantages of dynamically typed languages:

// 1. Runtime Errors: Type errors are only caught at runtime, which can lead
// to more bugs
// 2. Performance Overhead: Generally slower due to type checking at runtime